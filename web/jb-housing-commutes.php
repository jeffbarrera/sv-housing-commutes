<p>Sean MacDonald&#39;s shift as a Palo Alto fire captain starts at 8 a.m. By 4 a.m. he's out the door and on the road. It's 147 miles from his home in Roseville — northeast of Sacramento — to the fire station. Leave any later, and he risks getting stuck in traffic.</p>

<p>&ldquo;The drive stinks, no easy way about it,&rdquo; he said, but &ldquo;that&#39;s the price you pay to have a job in the Bay Area and raise a family.&rdquo;</p>

<p>Facing sky-high housing costs in the city they serve, long commutes have become the norm among MacDonald&#39;s fellow firefighters. &ldquo;We&#39;ve got guys living in Shingle Springs, Rocklin, Citrus Heights, Grass Valley,&rdquo; he said, ticking off a dozen places over 100 miles from Palo Alto.</p>

<p>Some of the younger firefighters live closer to Palo Alto, but they share apartments to save on rent. And even then, MacDonald said, &ldquo;what they&#39;re paying in rent is more than what I&#39;m paying on a mortgage.&rdquo;</p>

<p>&ldquo;Housing prices here are so, so expensive,&rdquo; agreed firefighter Matt Goglio, who makes a 105-mile journey from Oakdale, past Modesto. &ldquo;We&#39;re basically being driven out of the area just due to the wages and the housing costs.&rdquo;</p>

<p>It&#39;s not just firefighters who are being priced out by Silicon Valley&#39;s cost of living. As the Peninsula struggles to house a rapidly growing population, many people — especially the middle-class public servants and low-wage workers who keep the region running smoothly — have been moving away to more affordable places.</p>

<p>Yet since their jobs are still back on the Peninsula, these workers are spending more time behind the wheel — at least an hour each way for over 200,000 people.</p>

<h3>Rising Rents</h3>

<p>The booming tech economy has drawn more workers to Silicon Valley, but housing construction hasn&#39;t kept pace. Between 2010 and 2014, Santa Clara and San Mateo counties gained around 167,000 new jobs, but only added 22,000 additional housing units.</p>

