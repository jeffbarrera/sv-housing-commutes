<p>Many workers are moving even further out, to the East Bay or beyond, noted Matt Regan, a regional planning expert at the business-backed Bay Area Council.</p>

<p>&ldquo;You only have to drive on (Interstate) 580 at 4:30 in the morning and just look at the headlights coming over the Altamont Pass,&rdquo; he said, &ldquo;to know that that&#39;s where we&#39;re building our affordable housing, on the other side of the hill.&rdquo;</p>

<h3>Disparate Commutes</h3>

<p>This growth in places far from core Peninsula cities has driven up commute times, since more workers are traveling longer distances on increasingly congested roads. Between 2010 and 2014, the proportion of workers who spent an hour or more getting to their jobs jumped from 10 to nearly 15 percent.</p>

<p>Vautin noted that there&#39;s an important income disparity in those commute times. The highly paid technical and business services workers who live in Silicon Valley have relatively short commute times, since they typically work nearby. It&#39;s middle- and lower-income workers — teachers and firefighters, security guards at tech campuses, waiters at restaurants — who have been priced out of the Peninsula and are spending much more time in traffic:</p>

