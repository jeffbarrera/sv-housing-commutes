<h3>Stemming the Sprawl</h3>

<p>Policy experts argue that the key to bringing commute times under control is to address the underlying housing shortage. By building higher-density apartment buildings and townhouses near worksites and along public transit corridors, planners hope to house more workers on the Peninsula and reduce the need to commute from outlying areas.</p>

<p>That kind of dense housing can be a hard sell in suburban Silicon Valley, where residents often fight new construction they fear will worsen traffic and change the character of their communities.</p>

<p>&ldquo;As soon as a project proponent arrives at City Hall with a multi-family development idea, the pitchforks are sharpened and the flaming torches are lit,&rdquo; said Regan. Because of such opposition, many attempts to build new housing have been scaled down or abandoned altogether.</p>

<p>Still, there are signs the worsening housing crunch is making residents more amenable to growth. Voters in Mountain View elected a pro-housing majority to the city council in 2014, community groups in several Peninsula cities are organizing to support new development, and Redwood City is building a cluster of tall new office and apartment buildings near its downtown Caltrain station.</p>

<p>Ultimately, how much Silicon Valley cities are willing to grow will affect everything from the region&#39;s carbon footprint, to whether it can retain teachers, to whether firefighters like Goglio will be around in a disaster.</p>

<p>&ldquo;The Bay Area traditionally has not been the fastest growing region in the state,&rdquo; said Vautin, and so it has been ill-prepared for the influx of new arrivals. &ldquo;This is the critical issue of the decade here.&rdquo;</p>

